let alunos = ["cristiano","Lobato","Rafael","Marcus"]
console.log(alunos);

// REMOVER ULTIMA COISA ARMAZENADA
console.log("== POP ==");
alunos.pop()
console.log(alunos);

// REMOVER 1 COISA ARMAZENADA
console.log("== shift ==");
alunos.shift()
console.log(alunos);

// Adicionar algo no final
console.log("== push ==");
alunos.push("Yago")
console.log(alunos);

// Adicionar algo no inicio
console.log("== unshift ==");
alunos.unshift("Luiz")
console.log(alunos);