// Exercício 3 — Função com parâmetro
// 
// Crie uma função chamada `apresentarNome`.
// A função deve receber um parâmetro chamado `nome` e exibir uma mensagem no formato:
// Olá, Ana!
// Exemplo de chamada:
// apresentarNome("Ana");

function apresentarNome(nome) {
    console.log(`Olá, ${nome}`);
}
apresentarNome("Caue");
apresentarNome("Lobato");
