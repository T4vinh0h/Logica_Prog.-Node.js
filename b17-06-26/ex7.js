// Exercício 7 — Guardando o retorno em uma variável
// 
// Crie uma função chamada `calcularDobro`.
// A função deve receber um número e retornar o dobro dele.
// Depois, guarde o retorno em uma variável chamada `resultado`.
// Exiba a variável no console.
// Exemplo:
// let resultado = calcularDobro(5);
// console.log(resultado);
// Resultado esperado:
// 10

function calcularDobro(numero) {
    return numero * 2;
}
let resultado = calcularDobro(10);
console.log(`Receba o dobro: ${resultado}`);


