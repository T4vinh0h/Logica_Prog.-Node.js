// Exercício 8 — console.log dentro da função
// 
// Crie uma função chamada `mostrarTriplo`.
// A função deve receber um número e exibir o triplo desse número usando `console.log`.
// Exemplo:
// mostrarTriplo(4);
// Resultado esperado:
// 12

function mostrarTriplo(numero) {
    console.log(`Triplicar: ${numero * 3}`);
}
mostrarTriplo(3);