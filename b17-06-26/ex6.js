// Exercício 6 — Função com retorno
// 
// Crie uma função chamada `somar`.
// A função deve receber dois números como parâmetros e retornar a soma deles.
// Exemplo:
// console.log(somar(5, 3));
// Resultado esperado:
// 8

function somar(n1, n2) {
    return n1 + n2;
}
console.log(`A soma: ${somar(2, 2)}`);
