// Exercício 13 — Função que calcula média
// 
// Crie uma função chamada `calcularMedia`.
// A função deve receber duas notas como parâmetros e retornar a média.
// Exemplo:
// console.log(calcularMedia(8, 7));
// Resultado esperado:
// 7.5

function calcularMedia(n1, n2) {
    return (n1 + n2) / 2;
}

console.log(calcularMedia(8, 7));
