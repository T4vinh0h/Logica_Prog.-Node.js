// 3. Verifique se duas variáveis possuem o mesmo valor usando `==`.

let n1 = +(prompt(`Digite um Numero`));
let n2 = +(prompt(`Digite outro Numero:`));
const comparar = (n1 == n2 || n2 == n1);
if (comparar = comparar) {
    console.log(`MESMO VALOR`);
} else {
    console.log(`NÃO É IGUAL!`);
}



