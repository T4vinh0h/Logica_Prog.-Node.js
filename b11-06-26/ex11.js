// 11. Percorrendo com forEach()
// Crie um vetor com 5 nomes.
// Use forEach() para mostrar cada nome no console.
// Exemplo de saída:
// Nome: Ana
// Nome: Bruno
// Nome: Carlos

let nomes = ["Ana", "Bruno", "Carlos", "Daniela", "Eduardo"];

nomes.forEach(nome => {
    console.log(`Nome: ${nome}`);
});
