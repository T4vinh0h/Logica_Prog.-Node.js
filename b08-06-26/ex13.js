// 13. Mostrando todos os elementos com for
// Crie um array com 5 nomes de alunos.
// Depois, use um laço for para mostrar cada nome em uma linha diferente.
// Exemplo de saída:
// Ana
// Bruno
// Carlos
// Daniela
// Eduardo

let nomes = [ "Ana", "Bruno", "Carlos", "Daniela", "Eduardo"];
for (let i = 0; i <= 4; i++) {
    console.log(nomes[i]);
}
console.log(nomes.length);

